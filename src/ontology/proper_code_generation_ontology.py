    ontology.save_to_file("code_generation_ontology.xml", "xml")
    ontology.save_to_file("code_generation_ontology.json", "json-ld")

    print(f"  ✅ Turtle: code_generation_ontology.ttl")
    print(f"  ✅ XML: code_generation_ontology.xml")
    print(f"  ✅ JSON-LD: code_generation_ontology.json")

